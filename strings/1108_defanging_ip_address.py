# LeetCode 1108: Defanging an IP Address
# Date solved: Dec 4, 2025
# Approach: String Manipulation
# Explanation:
#   Replace every '.' in the given IP address with '[.]'. This prevents the IP
#   string from being interpreted as a clickable or executable address.
#   Python's replace() creates a new string since strings are immutable.
# Time Complexity: O(n)
# Space Complexity: O(n)
